    public void CargarBarra(){
        ac = (ActionEvent e) -> {
            x = x + 1;
            Progress.setValue(x);
            if(Progress.getValue()==100){
                Login obj  = new Login();
                obj.setVisible(true);
                dispose();
                t.stop();
            }
        };
        t = new Timer(10, ac);
        t.start();
    }

